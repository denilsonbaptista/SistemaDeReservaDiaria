package edu.unicesumar.map.psii;

/**
 *
 * @author Denilson
 */
public class Suite {
    int numeroDaSuite;
    String tipo;
    int capacidade;
    double valorDiaria;

    public int getNumeroDaSuite() {
        return numeroDaSuite;
    }

    public void setNumeroDaSuite(int numeroDaSuite) {
        this.numeroDaSuite = numeroDaSuite;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public int getCapacidade() {
        return capacidade;
    }

    public void setCapacidade(int capacidade) {
        this.capacidade = capacidade;
    }

    public double getValorDiaria() {
        return valorDiaria;
    }

    public void setValorDiaria(double valorDiaria) {
        this.valorDiaria = valorDiaria;
    }
    
}
